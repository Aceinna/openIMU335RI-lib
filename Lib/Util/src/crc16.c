/*******************************************
 * @brief 
 * 
 * @param data ==
 * @param len ==
 * @return uint16_t 
********************************************/
uint16_t CRC16(uint8_t data[], int32_t len)
{
	uint8_t uchCRCHi = 0xFFU ; 	// high byte of CRC initialized
	uint8_t uchCRCLo = 0xFFU ; 	// low byte of CRC initialized
	uint32_t uIndex; 			// will index into CRC lookup table
	uint32_t pos = 0U;
	uint8_t tmp;
// calculate the CRC
	while (len--)	{
    	tmp      = uchCRCHi ^ data[pos];
    	uIndex   = (uint32_t)tmp;
		pos++;
		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ;
		uchCRCLo = auchCRCLo[uIndex] ;
	}
	uint16_t retval = uchCRCHi;
	retval <<= 8U;
	retval |= uchCRCLo;
	return retval;
}


/** ****************************************************************************
 * @brief
 * @param buf     [in] - input value
 * @param length  [in] - crc seed
 * @return crc
 ******************************************************************************/
uint16_t CalculateCRC (uint8_t buf[], int32_t const  length)
{
	uint16_t crc = 0x1D0FU;  //non-augmented inital value equivalent to the augmented initial value 0xFFFF
	uint16_t tmp1;
	uint16_t tmp2;

	for (int32_t i=0; i < length; i++) {
		tmp1 = buf[i];
		tmp1 <<= 8U;
		crc ^= tmp1;

		for (int32_t j=0; j<8; j++) {
			if (crc & 0x8000U) {
                crc = (uint16_t)((crc << 1U) ^ 0x1021U);
            }
			else {
                crc = (uint16_t)(crc << 1U);
            }
		}
	}

	tmp1 = (uint16_t)(crc << 8U) & 0xFF00U;
	tmp2 = (crc >> 8U) & 0xFFU;
	return tmp1 | tmp2;

}
